package main;

public class SetofVariables {

	public static void main(String[] args) {
		
		int integer = 33333;
		long large = 42561230L;
		short small = 128;
		byte tiny = 2;
		float f = .0925F;
		float decimal = 0.725F;
		double largeDouble = +6.022E23; // This is in scientific notation
		char character = 'A';
		boolean t = true;
		System.out.println("Integer is " + integer);
		System.out.println("Large is " + large);
		System.out.println("Small is " + small);
		System.out.println("Tiny is " + tiny);
		System.out.println("F is " + f);
		System.out.println("Decimal is " + decimal);
		System.out.println("Large Double is " + largeDouble);
		System.out.println("Character is " + character);
		System.out.println("T is " + t);
		
	}

}
